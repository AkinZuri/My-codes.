def calcular_media(temperatura_dia):
    return sum(temperatura_dia)/len(temperatura_dia)

dia_com_media_maior_que_25 = []

for i, temperatura_dia in enumerate(temperaturas):
    media = calcular_media(temperatura_dia)
    if media >= 25:
        dia_com_media_maior_que_25.append(i+1)
        
resultado = ""
for dia in dia_com_media_maior_que_25:
    resultado += f"Dia {dia}: temperatura média maior que 25º C.\n"
    
print(resultado)
